#import "ETAppDelegate.h"

#import "ETMainViewController.h"

@implementation ETAppDelegate

- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions
{
    self.window = [[UIWindow alloc] initWithFrame:[[UIScreen mainScreen] bounds]];
    // Override point for customization after application launch.
    if ([[UIDevice currentDevice] userInterfaceIdiom] == UIUserInterfaceIdiomPhone) {
        self.viewController = [[ETMainViewController alloc] initWithNibName:@"ETMainViewController_iPhone" bundle:nil];
    } else {
        self.viewController = [[ETMainViewController alloc] initWithNibName:@"ETMainViewController_iPad" bundle:nil];
    }
    
    [self.window setRootViewController:self.viewController];
    [self.window makeKeyAndVisible];
    return YES;
}

